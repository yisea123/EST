/*============================
  I2c start condition          
 SDA high->low while SCL=high 
     _________                 
  SCL         \_________       
     _______                  
  SDA       \___________      
============================*/

void iic_start(bit cd)				//1send IIC start condition
{
	sda=1;//_nop_( );_nop_( );_nop_( );_nop_( );_nop_( );
	sck=1;//_nop_( );_nop_( );_nop_( );_nop_( );_nop_( );
	sda=0;_nop_( );//_nop_( );_nop_( );_nop_( );_nop_( );_nop_( );_nop_( );
	sck=0;_nop_( );_nop_( );_nop_( );_nop_( );
	sda=0;sck=1;sck=0;
	sda=1;sck=1;sck=0;
	sda=1;sck=1;sck=0;
	sda=1;sck=1;sck=0;//------------sequence header(0111B)

        sda=1;sck=1;sck=0;
	sda=0;sck=1;sck=0;     //------------device address [1,0] 
	sda=cd;sck=1;sck=0;//cd=0(control);  cd=1(data)	//----------------------cd control
	sda=0;sck=1;sck=0;//write mode(1 read mode)
    
	sck=1;_nop_();_nop_();       //---signal A-----------//
    sck=0;_nop_();_nop_();
}

//----------------------write_iic{cd=0(control);  cd=1(data)}---------------------//
void iic_send(unsigned char y)
{
	unsigned char i;
	for(i=0;i<8;i++)
	{
		if(y&0x80)
			sda=1;
		else
		    sda=0;
		sck=1;
		sck=0;
		y=y<<1;
	}  
     sck=1;       //---signal A-----------//
     _nop_();
     _nop_();       
     sck=0;
     _nop_();
     _nop_();

}

/*============================
  I2c stop condition          
  SDA low->high while SCL=high 
            _________                 
  SCL______/       
             _______                  
  SDA_______/      
============================*/

void iic_stop()
{
        sda=0;_nop_(); _nop_();
        sck=0;_nop_();_nop_();
        sck=1;_nop_();_nop_();_nop_();_nop_();_nop_();
        sda=1;_nop_();_nop_();_nop_();_nop_();
}

